// This is our main class - the container for our program
public class HelloWorld {
    
    // This is the main method - the starting point where Java begins execution
    // public: accessible from anywhere
    // static: belongs to the class itself, not to any specific instance
    // void: doesn't return any value
    // String[] args: accepts command-line arguments as an array of strings
    public static void main(String[] args) {
        
        // This line prints text to the console and moves to a new line
        System.out.println("Hello, World!");
        
        // You can add more statements here
        System.out.println("This is my first Java program!");
        System.out.println("It's running successfully!");
    }
}